'''
abs (Absolute)

Function to calculate magnitude of a number. 

Syntax:
abs(number)

Parameters:
number: The number to calculate magnitude.

Returns:
Absolute value of the number.
'''

intger_number = -10
floating_number = -3.14
complex_number   = 3 + 4j

print(abs(intger_number))
print(abs(floating_number))
print(abs(complex_number))